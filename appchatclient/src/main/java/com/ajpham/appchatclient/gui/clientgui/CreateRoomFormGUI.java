    private void lbFindMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbFindMouseClicked
        String nameMember = this.tfFindMember.getText().trim();
        if (!nameMember.equals("") && !nameMember.equals(_nettyClient.getUserName())) {
            //send message to server
            _serviceUser.sendMessageFindUser(_token, nameMember);
        }
    }//GEN-LAST:event_lbFindMouseClicked

