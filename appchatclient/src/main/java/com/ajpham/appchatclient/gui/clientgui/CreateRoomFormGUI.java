    //set image
    public void setPicture(JLabel label, byte[] avatar) {
        if (avatar != null) {
            Image img = new ImageIcon(avatar).getImage().
                    getScaledInstance(lbAvatar.getSize().width, lbAvatar.getSize().height, BufferedImage.SCALE_SMOOTH);
            ImageIcon icon = new ImageIcon(img);
            lbAvatar.setIcon(icon);
            lbAvatar.setText("");
        } else {
            lbAvatar.setText("Choose Avatar");
        }
    }

