    private void lbAvatarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbAvatarMouseClicked
        _pathFileAvatar = _handleAvatar.chooseAvatar();
        byte[] avatar;
        try {
            avatar = _handleAvatar.convertFile(_pathFileAvatar);
            setPicture(lbAvatar, avatar);
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_lbAvatarMouseClicked

