    public void resetRegister(boolean flag) {
        this.lbNotify.setVisible(!flag);
        if (flag) {
            this.tfNickname.setText("");
            this.tfUsername.setText("");
            this.tfPassword.setText("");
            _pathFileAvatar = "";
        }
    }

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        if (JOptionPane.showConfirmDialog(this,
                "Are you sure to close Register form?", "Really Closing?",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
            LoginFormGUI signinGUI;
            signinGUI = new LoginFormGUI(_nettyClient);
            signinGUI.setVisible(true);
            this.setVisible(false);
            this.dispose();
        } else {
            this.resetRegister(true);
            setPicture(this.lbAvatar, null);
        }

    }//GEN-LAST:event_btnCancelActionPerformed

    //set image
    public void setPicture(JLabel label, byte[] avatar) {
        if (avatar != null) {
            Image img = new ImageIcon(avatar).getImage().
                    getScaledInstance(label.getSize().width, label.getSize().height, BufferedImage.SCALE_SMOOTH);
            ImageIcon icon = new ImageIcon(img);
            label.setIcon(icon);
            label.setText("");
        }
    }

    private void lbAvatarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbAvatarMouseClicked
        try {
            _pathFileAvatar = _handleAvatar.chooseAvatar();
            byte[] avatar;
            avatar = _handleAvatar.convertFile(_pathFileAvatar);
            setPicture(lbAvatar, avatar);
        } catch (IOException ex) {
            Logger.getLogger(RegisterFormGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lbAvatarMouseClicked

