    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        this._name = this.tfNickname.getText().trim();
        this._userName = this.tfUsername.getText().trim();
        this._password = String.valueOf(this.tfPassword.getPassword()).trim();
        if (!this._userBUS.checkUserRegister(this._name, this._userName, this._password, _pathFileAvatar)) {
            this.lbNotify.setVisible(true);
        } else {
            try {
                _serviceUser.sendMessageRegister(_name, _userName, _password, _handleAvatar.convertFile(_pathFileAvatar));
            } catch (IOException ex) {
                Logger.getLogger(RegisterFormGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSignupActionPerformed

